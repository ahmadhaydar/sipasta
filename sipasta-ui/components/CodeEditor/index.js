export default function CodeEditor() {
  return (
    <CodeMirror
      value={code}
      height="200px"
      theme={githubLight}
      extensions={[markdown({ base: markdownLanguage, codeLanguages: languages })]}
      onChange={(value, viewUpdate) => {
        console.log('value:', value);
      }}
    />
  )
}